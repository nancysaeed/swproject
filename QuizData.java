package io.javasprings.springbootEval;

public class QuizData {

	String Title;
    String SkillType;
    
 public QuizData(){}
 
 public void setTitle(String cn)
 {
     Title = cn;
 }
 public String getTitle()
 {
     return Title;
 }
 
 public void setSkillType(String Skill)
 {
     SkillType  = Skill;
 }
 public String getSkillType()
 {
     return SkillType ;
 }
}
